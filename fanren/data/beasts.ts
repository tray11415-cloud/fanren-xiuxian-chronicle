// 妖獸境界 → 簡化五階 rank（0 煉氣 … 4 化神含以上）
const BEAST_RANK: Record<string, number> = { 煉氣期: 0, 炼气期: 0, 築基期: 1, 筑基期: 1, 金丹期: 2, 结丹期: 2, 結丹期: 2, 元婴期: 3, 元嬰期: 3, 化神期: 4, 煉虛期: 4, 炼虚期: 4, 合體期: 4, 合体期: 4, 大乘期: 4 };
export function beastRank(b: NamedBeast): number { return BEAST_RANK[b.realm] ?? 2; }
/** 取一隻 rank 與目標相近（±1）的具名妖獸；無則回 null。 */
export function namedBeastForRank(rank: number, rnd: number): NamedBeast | null {
  const pool = NAMED_BEASTS.filter((b) => Math.abs(beastRank(b) - rank) <= 1);
  if (!pool.length) return null;
  return pool[Math.floor(rnd * pool.length) % pool.length];
}
