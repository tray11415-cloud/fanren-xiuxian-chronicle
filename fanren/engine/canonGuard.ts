export interface CanonScreen {
  decision: 'reframe' | 'reject';
  reason: string;
  reframedAs?: string;
  analog?: string;
  term: string;
}

/** 篩查輸入是否出戲。回傳 null 表示合乎世界觀。 */
export function screenCanon(text: string): CanonScreen | null {
  const t = text || '';
  for (const r of REJECT_RULES) {
    const m = t.match(r.re);
    if (m) return { decision: 'reject', reason: r.reason, term: m[0] };
  }
  for (const r of REFRAME_RULES) {
    const m = t.match(r.re);
    if (m) {
      return {
        decision: 'reframe',
        reason: `「${m[0]}」非此界之物——${r.hint}。`,
        analog: r.analog,
        term: m[0],
        reframedAs: `（你心中所想的「${m[0]}」，於此修仙界並無此物；姑且以${r.analog}視之）`,
      };
    }
  }
  return null;
}
